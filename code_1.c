/* 
C program to print ASCII value of character.

ASCII stands for American Standard Code for Information Interchange. ASCII is a standard that assigns each character a numeric value. For example, the ASCII value of 'A' is 65.

For more details, visit ASCII Table.
*/

#include <stdio.h>

int main(int argc, char const *argv[])
{
    char a = 120;
    printf("%d", a);
    return 0;
}
